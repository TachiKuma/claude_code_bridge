from __future__ import annotations

from typing import Protocol, TypedDict

from .endpoint import EndpointRef


class PeerEvidence(TypedDict, total=False):
    kind: str
    same_user: bool
    detail: str


class ControlPlaneConnection(Protocol):
    def settimeout(self, value: float | None) -> None: ...
    def sendall(self, payload: bytes) -> None: ...
    def recv(self, size: int) -> bytes: ...
    def close(self) -> None: ...
    def __enter__(self) -> 'ControlPlaneConnection': ...
    def __exit__(self, exc_type, exc, tb) -> None: ...


class ControlPlaneListener(Protocol):
    endpoint: EndpointRef

    def settimeout(self, value: float | None) -> None: ...
    def accept(self) -> tuple[ControlPlaneConnection, PeerEvidence]: ...
    def close(self) -> None: ...
    def fileno(self) -> int: ...


class ControlPlaneTransport(Protocol):
    endpoint: EndpointRef

    def connect(self, *, timeout_s: float) -> ControlPlaneConnection: ...
    def is_connectable(self, *, timeout_s: float = 0.2) -> bool: ...
    def listen(self) -> ControlPlaneListener: ...
    def unlink_bound_endpoint(self, *, bound_identity) -> None: ...
    def bootstrap_readiness_probe(self, server, *, timeout_s: float): ...
